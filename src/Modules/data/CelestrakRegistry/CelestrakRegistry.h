#pragma once
#include "Modules/data/DataTypes.h"
#include <vector>
#include <string>

/**
 * @brief Получить полный реестр каталогов Celestrak
 *
 * Возвращает список категорий и групп спутников,
 * доступных на сервисе Celestrak. Структура реестра:
 * категория → группы.
 * Каждая группа соответствует параметру GROUP в API Celestrak.
 *
 * Используется для:
 * - отображения меню пользователю
 * - сопоставления пользовательского ввода с реальными группами
 * - формирования URL для загрузки
 *
 * @return Константная ссылка на вектор категорий каталогов
 */
const std::vector<CelestrakCategory>& getCelestrakCatalogRegistry();

/**
 * @brief Вывести список доступных каталогов в консоль
 *
 * Формирует меню со списком категорий и входящих в них групп.
 */
void printCelestrakMenu();

/**
 * @brief Преобразовать пользовательский ввод в список групп
 *
 * Обрабатывает введенную строку и определяет,
 * какие каталоги необходимо выбрать.
 *
 * Поддерживает:
 * - выбор по названию группы
 * - выбор по названию категории (включает все группы внутри)
 * - выбор нескольких элементов через запятую
 * - специальные команды: "all", "update all"
 *
 *
 * @param input Строка пользовательского ввода
 * @return Вектор выбранных групп (без дубликатов)
 */
std::vector<CelestrakGroup> resolveUserSelection(const std::string& input);